create table product_category(product_category_id int8 generated by default as identity,
                              product_category_name varchar(255),
                              primary key (product_category_id));

create table product_subcategory (product_subcategory_id int8 generated by default as identity,
                                  product_subcategory_name varchar(255),
                                  primary key (product_subcategory_id));

create table products (product_id int8 generated by default as identity,
                       calorie_value float4,
                       carbohydrates_value float4,
                       fats_value float4,
                       glycemic_index int4,
                       product_name varchar(255),
                       ready_meal int4,
                       squirrels_value float4,
                       product_category_id int8,
                       product_subcategory_id int8,
                       primary key (product_id));

create table risks (risk_id int8 generated by default as identity,
                    risk_name varchar(255),
                    primary key (risk_id));

create table supplement_categories (supplement_category_id int8 generated by default as identity,
                                    supplement_category_name varchar(255),
                                    primary key (supplement_category_id));

create table supplements (supplement_id int8 generated by default as identity,
                          effect_on_body varchar(5000),
                          feature varchar(5000),
                          supplement_code varchar(255),
                          supplement_name varchar(32000),
                          risk_id int8,
                          supplement_category_id int8,
                          primary key (supplement_id));

alter table if exists products add constraint FK_product_category_id
    foreign key (product_category_id) references product_category;

alter table if exists products add constraint FK_product_subcategory_id
    foreign key (product_subcategory_id) references product_subcategory;

alter table if exists supplements add constraint FK_risk_id
    foreign key (risk_id) references risks;

alter table if exists supplements add constraint FK_supplement_category_id
    foreign key (supplement_category_id) references supplement_categories;